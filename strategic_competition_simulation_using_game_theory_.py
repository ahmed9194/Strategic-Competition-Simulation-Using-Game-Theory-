class ExtensiveFormGame:

    def __init__(self, game):
        self.game = game

    # -----------------------------------------------------
    # Tree Structure Representation
    # -----------------------------------------------------
    def build_game_tree(self):

        tree = {
            "Firm A": {
                "Low Quantity": {
                    "Firm B": [
                        "Low",
                        "Medium",
                        "High"
                    ]
                },

                "Medium Quantity": {
                    "Firm B": [
                        "Low",
                        "Medium",
                        "High"
                    ]
                },

                "High Quantity": {
                    "Firm B": [
                        "Low",
                        "Medium",
                        "High"
                    ]
                }
            }
        }

        return tree

    # -----------------------------------------------------
    # Print Tree Structure
    # -----------------------------------------------------
    def print_game_tree(self):

        tree = self.build_game_tree()

        print(
            "\n========== EXTENSIVE FORM TREE =========="
        )

        for action in tree["Firm A"]:

            print(f"\nFirm A chooses: {action}")

            responses = tree["Firm A"][action]["Firm B"]

            for response in responses:
                print(f"   -> Firm B responds: {response}")

    # -----------------------------------------------------
    # Draw Game Tree
    # -----------------------------------------------------
    def draw_game_tree(self):

        plt.figure(figsize=(12, 7))

        plt.text(0.5, 0.9, "Firm A", ha='center', fontsize=14)

        plt.plot([0.5, 0.2], [0.85, 0.65])
        plt.plot([0.5, 0.5], [0.85, 0.65])
        plt.plot([0.5, 0.8], [0.85, 0.65])

        plt.text(0.2, 0.62, "Low", ha='center')
        plt.text(0.5, 0.62, "Medium", ha='center')
        plt.text(0.8, 0.62, "High", ha='center')

        plt.plot([0.2, 0.1], [0.58, 0.4])
        plt.plot([0.2, 0.2], [0.58, 0.4])
        plt.plot([0.2, 0.3], [0.58, 0.4])

        plt.plot([0.5, 0.4], [0.58, 0.4])
        plt.plot([0.5, 0.5], [0.58, 0.4])
        plt.plot([0.5, 0.6], [0.58, 0.4])

        plt.plot([0.8, 0.7], [0.58, 0.4])
        plt.plot([0.8, 0.8], [0.58, 0.4])
        plt.plot([0.8, 0.9], [0.58, 0.4])

        positions = [
            (0.1, "Low"),
            (0.2, "Medium"),
            (0.3, "High"),
            (0.4, "Low"),
            (0.5, "Medium"),
            (0.6, "High"),
            (0.7, "Low"),
            (0.8, "Medium"),
            (0.9, "High")
        ]

        for x, label in positions:
            plt.text(x, 0.35, label, ha='center')

        plt.title("Extensive Form Game Tree")

        plt.axis('off')

        plt.savefig("extensive_form_tree.png")

        plt.show()

    # -----------------------------------------------------
    # Stackelberg Equilibrium
    # -----------------------------------------------------
    def stackelberg_equilibrium(self):

        a = self.game.params.a
        b = self.game.params.b
        c1 = self.game.params.c1
        c2 = self.game.params.c2

        q1 = (a - 2 * c1 + c2) / (2 * b)

        q2 = self.game.best_response_firm2(q1)

        price = self.game.market_price(q1, q2)

        profit1 = self.game.profit_firm1(q1, q2)
        profit2 = self.game.profit_firm2(q1, q2)

        return {
            "leader_quantity": q1,
            "follower_quantity": q2,
            "price": price,
            "leader_profit": profit1,
            "follower_profit": profit2
        }


