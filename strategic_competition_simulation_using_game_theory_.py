import numpy as np
import matplotlib.pyplot as plt
from dataclasses import dataclass


# =========================================================
# MARKET PARAMETERS
# =========================================================

@dataclass
class MarketParameters:
    a: float = 100
    b: float = 1
    c1: float = 20
    c2: float = 20


# =========================================================
# COURNOT COMPETITION MODEL
# =========================================================

class CournotCompetition:

    def __init__(self, params: MarketParameters):
        self.params = params

    # -----------------------------------------------------
    # Market Price Function
    # -----------------------------------------------------
    def market_price(self, q1, q2):
        return self.params.a - self.params.b * (q1 + q2)

    # -----------------------------------------------------
    # Profit Function for Firm A
    # -----------------------------------------------------
    def profit_firm1(self, q1, q2):
        p = self.market_price(q1, q2)
        return (p - self.params.c1) * q1

    # -----------------------------------------------------
    # Profit Function for Firm B
    # -----------------------------------------------------
    def profit_firm2(self, q1, q2):
        p = self.market_price(q1, q2)
        return (p - self.params.c2) * q2

    # -----------------------------------------------------
    # Best Response Function for Firm A
    # -----------------------------------------------------
    def best_response_firm1(self, q2):

        return max(
            0,
            (self.params.a - self.params.c1 - self.params.b * q2)
            / (2 * self.params.b)
        )

    # -----------------------------------------------------
    # Best Response Function for Firm B
    # -----------------------------------------------------
    def best_response_firm2(self, q1):

        return max(
            0,
            (self.params.a - self.params.c2 - self.params.b * q1)
            / (2 * self.params.b)
        )

    # -----------------------------------------------------
    # Nash Equilibrium
    # -----------------------------------------------------
    def compute_nash_equilibrium(self):

        a = self.params.a
        b = self.params.b
        c1 = self.params.c1
        c2 = self.params.c2

        q1_star = (a - 2 * c1 + c2) / (3 * b)
        q2_star = (a - 2 * c2 + c1) / (3 * b)

        p_star = self.market_price(q1_star, q2_star)

        pi1 = self.profit_firm1(q1_star, q2_star)
        pi2 = self.profit_firm2(q1_star, q2_star)

        return {
            "q1": q1_star,
            "q2": q2_star,
            "price": p_star,
            "profit1": pi1,
            "profit2": pi2
        }

    # -----------------------------------------------------
    # Normal Form Representation
    # -----------------------------------------------------
    def generate_payoff_matrix(self, strategies):

        matrix = {}

        for q1 in strategies:

            matrix[q1] = {}

            for q2 in strategies:

                p1 = self.profit_firm1(q1, q2)
                p2 = self.profit_firm2(q1, q2)

                matrix[q1][q2] = (
                    round(p1, 2),
                    round(p2, 2)
                )

        return matrix

    # -----------------------------------------------------
    # Print Payoff Matrix
    # -----------------------------------------------------
    def print_payoff_matrix(self, strategies):

        matrix = self.generate_payoff_matrix(strategies)

        print("\n========== NORMAL FORM PAYOFF MATRIX ==========")

        for q1 in matrix:

            print(f"\nFirm A Quantity = {q1}")

            for q2 in matrix[q1]:

                print(
                    f"Firm B Quantity = {q2}"
                    f" --> Payoffs {matrix[q1][q2]}"
                )

    # -----------------------------------------------------
    # Dominated Strategies for Firm A
    # -----------------------------------------------------
    def dominated_strategies_firm1(self, strategies):

        dominated = []

        for s1 in strategies:

            for s2 in strategies:

                if s1 == s2:
                    continue

                dominated_flag = True

                for opponent in strategies:

                    payoff1 = self.profit_firm1(s1, opponent)
                    payoff2 = self.profit_firm1(s2, opponent)

                    if payoff1 >= payoff2:
                        dominated_flag = False
                        break

                if dominated_flag:
                    dominated.append((s1, s2))

        return dominated

    # -----------------------------------------------------
    # Dominated Strategies for Firm B
    # -----------------------------------------------------
    def dominated_strategies_firm2(self, strategies):

        dominated = []

        for s1 in strategies:

            for s2 in strategies:

                if s1 == s2:
                    continue

                dominated_flag = True

                for opponent in strategies:

                    payoff1 = self.profit_firm2(opponent, s1)
                    payoff2 = self.profit_firm2(opponent, s2)

                    if payoff1 >= payoff2:
                        dominated_flag = False
                        break

                if dominated_flag:
                    dominated.append((s1, s2))

        return dominated

    # -----------------------------------------------------
    # Plot Best Response Curves
    # -----------------------------------------------------
    def plot_best_response_curves(self):

        q_values = np.linspace(0, 100, 300)

        br1 = [
            self.best_response_firm1(q)
            for q in q_values
        ]

        br2 = [
            self.best_response_firm2(q)
            for q in q_values
        ]

        equilibrium = self.compute_nash_equilibrium()

        plt.figure(figsize=(10, 7))

        plt.plot(
            q_values,
            br1,
            linewidth=2,
            label="Firm A Best Response"
        )

        plt.plot(
            br2,
            q_values,
            linewidth=2,
            label="Firm B Best Response"
        )

        plt.scatter(
            equilibrium["q2"],
            equilibrium["q1"],
            s=150,
            label="Nash Equilibrium"
        )

        plt.xlabel("Firm B Quantity")
        plt.ylabel("Firm A Quantity")

        plt.title(
            "Best Response Curves and Nash Equilibrium"
        )

        plt.legend()
        plt.grid(True)

        plt.savefig("best_response_curves.png")

        plt.show()

    # -----------------------------------------------------
    # Plot Profit vs Strategy
    # -----------------------------------------------------
    def plot_profit_vs_strategy(self):

        q = np.linspace(0, 80, 300)

        fixed_q2 = 20

        profits = [
            self.profit_firm1(x, fixed_q2)
            for x in q
        ]

        plt.figure(figsize=(10, 7))

        plt.plot(
            q,
            profits,
            linewidth=2
        )

        plt.xlabel("Firm A Quantity")
        plt.ylabel("Profit")

        plt.title(
            "Profit vs Strategy for Firm A"
        )

        plt.grid(True)

        plt.savefig("profit_vs_strategy.png")

        plt.show()

    # -----------------------------------------------------
    # Plot Profit Surface
    # -----------------------------------------------------
    def plot_profit_surface(self):

        q1 = np.linspace(0, 80, 100)
        q2 = np.linspace(0, 80, 100)

        profits = []

        for x in q1:

            row = []

            for y in q2:

                row.append(
                    self.profit_firm1(x, y)
                )

            profits.append(row)

        profits = np.array(profits)

        plt.figure(figsize=(10, 7))

        contour = plt.contourf(
            q2,
            q1,
            profits,
            levels=20
        )

        plt.colorbar(contour)

        plt.xlabel("Firm B Quantity")
        plt.ylabel("Firm A Quantity")

        plt.title(
            "Profit Surface for Firm A"
        )

        plt.savefig("profit_surface.png")

        plt.show()


